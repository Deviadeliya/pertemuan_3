public class perulangan_do_while {
    
    public static void main(String[] args) {
        int angka = 1;

        System.out.println("Menampilkan angka 1 sampai 5 menggunakan do-while:");
        
        do {
            System.out.println("Angka: " + angka);
            angka++;
        } while (angka <= 5);
    }
}

